// biome-ignore-all lint/suspicious/noConsole: Dev tools

import { readFileSync, renameSync, unlinkSync, writeFileSync } from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { colors, fonts } from "../src/tokens.ts";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const OUTPUT_PATH = path.join(__dirname, "../src/tailwind.css");

function fail(message) {
	console.error(`\x1b[31m✗\x1b[0m ${message}`);
	process.exit(1);
}

function validateTokens() {
	for (const [fontName, fontValue] of Object.entries(fonts)) {
		if (typeof fontValue !== "string") {
			fail(`Invalid font token ${fontName}: expected string`);
		}
	}

	for (const [familyName, family] of Object.entries(colors)) {
		for (const [shadeName, shade] of Object.entries(family)) {
			if (!shade || typeof shade.light !== "string" || typeof shade.dark !== "string") {
				fail(`Invalid color token ${familyName}-${shadeName}: expected light and dark strings`);
			}
		}
	}
}

function buildCss() {
	const lines = [
		"/**",
		" * This file is automatically generated.",
		" * DO NOT EDIT manually",
		" */",
		"",
		"@theme inline {",
	];

	for (const [fontName, fontValue] of Object.entries(fonts)) {
		const singleLine = `\t--font-${fontName}: ${fontValue};`;
		// Biome lineWidth=100, tabWidth=2: visual width of \t = 2
		const visualLength = singleLine.replace(/\t/g, "  ").length;
		if (visualLength <= 100) {
			lines.push(singleLine);
		} else {
			// Wrap: property name on its own line, values with double-tab indent
			lines.push(`\t--font-${fontName}:`);
			const items = fontValue.split(", ");
			let currentLine = "\t\t";
			for (let i = 0; i < items.length; i++) {
				const item = items[i];
				const isLast = i === items.length - 1;
				const candidate = currentLine === "\t\t" ? currentLine + item : `${currentLine}, ${item}`;
				const candidateVisual = (candidate + (isLast ? ";" : ",")).replace(/\t/g, "  ").length;
				if (candidateVisual > 100 && currentLine !== "\t\t") {
					lines.push(`${currentLine},`);
					currentLine = `\t\t${item}`;
				} else {
					currentLine = currentLine === "\t\t" ? currentLine + item : `${currentLine}, ${item}`;
				}
			}
			lines.push(`${currentLine};`);
		}
	}

	for (const [familyName, family] of Object.entries(colors)) {
		for (const shadeName of Object.keys(family)
			.map(Number)
			.sort((a, b) => a - b)) {
			lines.push(`\t--color-${familyName}-${shadeName}: var(--${familyName}-${shadeName});`);
		}
	}

	lines.push("}");
	lines.push("");
	lines.push("@media (prefers-reduced-motion: reduce) {");
	lines.push("\t*,");
	lines.push("\t*::before,");
	lines.push("\t*::after {");
	lines.push("\t\tanimation-duration: 0.01ms !important;");
	lines.push("\t\tanimation-iteration-count: 1 !important;");
	lines.push("\t\ttransition-duration: 0.01ms !important;");
	lines.push("\t}");
	lines.push("}");
	lines.push("");
	lines.push('@custom-variant dark (&:where([data-theme="dark"], [data-theme="dark"] *));');
	lines.push("");
	lines.push(":root {");

	for (const [familyName, family] of Object.entries(colors)) {
		for (const shadeName of Object.keys(family)
			.map(Number)
			.sort((a, b) => a - b)) {
			lines.push(`\t--${familyName}-${shadeName}: ${family[shadeName].light};`);
		}
	}

	lines.push("}");
	lines.push("");
	lines.push('[data-theme="dark"] {');

	for (const [familyName, family] of Object.entries(colors)) {
		for (const shadeName of Object.keys(family)
			.map(Number)
			.sort((a, b) => a - b)) {
			lines.push(`\t--${familyName}-${shadeName}: ${family[shadeName].dark};`);
		}
	}

	lines.push("}");

	return `${lines.join("\n")}\n`;
}

function main() {
	console.time(" \x1b[32m✓\x1b[0m Generated tailwind.css in ");

	try {
		validateTokens();

		const nextCss = buildCss();
		let currentCss = null;

		try {
			currentCss = readFileSync(OUTPUT_PATH, "utf-8");
		} catch (_error) {
			currentCss = null;
		}

		if (currentCss !== nextCss) {
			const tmpPath = `${OUTPUT_PATH}.tmp`;
			writeFileSync(tmpPath, nextCss, "utf-8");
			try {
				renameSync(tmpPath, OUTPUT_PATH);
			} catch (error) {
				try {
					unlinkSync(tmpPath);
				} catch (_unlinkError) {}
				throw error;
			}
		}

		console.timeEnd(" \x1b[32m✓\x1b[0m Generated tailwind.css in ");
	} catch (error) {
		const message = error instanceof Error ? error.message : String(error);
		fail(message);
	}
}

main();
